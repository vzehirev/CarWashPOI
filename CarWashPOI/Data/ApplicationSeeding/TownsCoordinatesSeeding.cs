        private readonly ApplicationDbContext dbContext;

        public TownsCoordinatesSeeding(ApplicationDbContext dbContext)
        {
            this.dbContext = dbContext;
        }

        public void SeedTownsCoordinates()
        {
            if (dbContext.Towns.Any(t => t.CoordinatesId == null))
            {
                Town[] towns = dbContext.Towns.Where(t => t.CoordinatesId == null).ToArray();

                foreach (Town town in towns)
                {
                    if (townsCoordinates.ContainsKey(town.Name))
                    {
                        town.Coordinates = new Coordinates
                        {
                            Latitude = townsCoordinates[town.Name][0],
                            Longitude = townsCoordinates[town.Name][1],
                        };
                    }
                }
                dbContext.SaveChanges();
            }
        }
